            <h2>PEMBAYARAN</h2>
            	<ol>
            	<li>
                	<strong>Berapa harga Cost Per Click SITTI?</strong><br />
                    		Nilai CPC (Cost Per Click) minimal adalah Rp600. Namun ada juga kata kunci yang nilai CPC-nya lebih dari Rp600. Hal ini dapat dipengaruhi oleh beberapa hal, salah satunya tingkat popularitas kata kunci tersebut.
                <br /><br /></li><li>
                    <strong>Berapa persen yang didapat untuk publisher?</strong><br />
                    Sharing revenue dari nilai CPC adalah 40% untuk SITTI dan 60% untuk publisher. Ini belum dipotong PPh.

                   <br /><br />
             	</li><li>
                    <strong>Script SITTI yang mana yang harus kami copy?</strong>
                    <br />Script SITTI yang harus dicopy yaitu kode html yang Anda dapatkan ketika membuat slot iklan baru.
                <br /><br /></li><li>    
                    <strong>Bagaimana cara perhitungan CPM?</strong>
                    <br />CPM dihitung berdasarkan per-1000 impresi.
                    
                <br /><br /></li><li>
                    <strong>Kapan SITTI melakukan pembayaran kepada publishernya? (Kapan pembayaran pertama akan saya dapatkan?)</strong>
                    <br />SITTI membayarkan pendapatan publisher terhitung 60 hari dari bulan publisher mendapatkan nominal Rp100.000. Hal ini tertera di Syarat & Ketentuan SITTI pada poin 11 tentang "Pembayaran".<br />
Contoh: Pendapatan Anda mencapai Rp100.000 pada bulan Juli 2011, maka SITTI akan melakukan pembayaran di akhir September 2011.

                <br /><br /></li><li>
                    <strong>Kapan pembayaran saya selanjutnya (bulan ke-2 dan seterusnya)?</strong>
                    <br />Pembayaran selanjutnya dilakukan sama dengan pembayaran pertama (60 hari setelah mencapai minimal pay out)<br />
Contoh: Pendapatan Anda selanjutnya (bulan Agustus 2011) telah mencapai nominal Rp100.000, maka SITTI akan melakukan pembayaran di akhir bulan Oktober 2011. Begitu seterusnya.

               <br /><br /></li><li>
                    <strong>Saya ingin berhenti jadi publisher SITTI, kapan sisa saldo saya akan dibayarkan?</strong><br />
                    Jika Perjanjian ini berakhir, SITTI akan membayarkan saldo penghasilan Anda dalam waktu kurang lebih sembilan puluh (90) hari setelah akhir bulan kalender di mana Persetujuan ini diakhiri oleh Anda (setelah SITTI menerima permintaan tertulis dari Anda, termasuk melalui surat elektronik, untuk mengakhiri Perjanjian tersebut) atau oleh SITTI. Dalam hal apa pun dan bagaimana pun, SITTI tidak akan mengirimkan pembayaran untuk saldo yang kurang dari Rp100.000.
               
                </li>
            </ol>
